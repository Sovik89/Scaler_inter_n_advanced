def searchInsert( A, B):
    n=len(A)
    
    #edge cases
    if n == 1:
        if A[0]<B:
            return 1
        else:
            return 0
    if n == 0:
        return 0
    if A[n-1]<B:
        return n
    
    low=0
    high=n-1
    mid =-1
    while low <= high:
        mid=(low+high)//2
        if A[mid] == B:
            return mid
        if A[mid]>B:
            high=mid-1
        if A[mid]<B:
            low=mid+1
            
    #print(mid,low,high)
    return low


